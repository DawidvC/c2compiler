#include <time.h>
#include <stdio.h>

#include "Utils/Utils.h"

using namespace C2;

u_int64_t Utils::getCurrentTime()
{
    struct timespec now;
    clock_gettime(CLOCK_MONOTONIC, &now);
    u_int64_t now64 = now.tv_sec;
    now64 *= 1000000;
    now64 += (now.tv_nsec/1000);
    return now64;
}

